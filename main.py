c_re = re.compile('(%s)' % '|'.join(cList.keys()))

def expand_contractions(text, c_re=c_re):
    def replace(match):
        return cList[match.group(0)]
    return c_re.sub(replace, text.lower())



def process(sentences):
    PRETRAINED_MODEL_PATH = 'data/lid.176.bin'
    model = fasttext.load_model(PRETRAINED_MODEL_PATH)
    processed_sentences = []
    for sentence in sentences:
        expanded_sentence = []
        expanded_sentence.append(expand_contractions(sentence[0]))
        predictions = model.predict(expanded_sentence)
       # print(predictions)
        if len(predictions) > 0:
            lang_code = predictions[0][0][0].split('__label__')[1]
            lang_name = languages.get(alpha_2=lang_code).name
            # Translate the sentence if it is not English
            if lang_name != 'English':
                processed_sentences.append(translate(expanded_sentence))
            else:
                processed_sentences.append(expanded_sentence[0])
    return processed_sentences


def translate(sentence):
    translator = Translator()
    translations = translator.translate(sentence, dest='en')
    for translation in translations:
        print(translation.origin, ' -> ', translation.text)
        return translation.text


